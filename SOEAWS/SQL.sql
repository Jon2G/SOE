	(
	(@PARENT_COMMENT_ID IS NULL AND PARENT_COMMENT_ID IS NULL)
	OR
	PARENT_COMMENT_ID=@PARENT_COMMENT_ID) AND
	(Row#>@OFFSET) 
	ORDER BY [Row#] DESC
	OFFSET @OFFSET ROWS FETCH NEXT 25 ROWS ONLY
END
