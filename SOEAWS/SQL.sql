@MAIL VARCHAR(100),
@SCHOOL_ID INT,
@DEVICE_KEY VARCHAR(100),
@BRAND VARCHAR(100),
@PLATFORM VARCHAR(100),
@MODEL  VARCHAR(100),
@D_NAME  VARCHAR(100),
@TYPE INT=1
)
AS
BEGIN
	SELECT @SCHOOL_ID=ID FROM SCHOOLS WHERE ID=@SCHOOL_ID
	IF @SCHOOL_ID<=0
	BEGIN
		SELECT 'INVALID_REQUEST','La escuela seleccionada es invalida'
		RETURN;
	END  
